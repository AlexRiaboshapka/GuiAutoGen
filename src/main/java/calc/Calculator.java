package calc;

public class Calculator {
    public Double getSum(Double a, Double b) {
        return a + b;
    }

    public Double getMultiplication(Double a, Double b) {
        return a * b;
    }

    public Double getDivision(Double a, Double b) {
        return a / b;
    }

    public Double getSubtraction(Double a, Double b) {
        return a - b;
    }
}
